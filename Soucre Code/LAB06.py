num1 = int(input("Input num1: "))
num2 = int(input("Input num2: "))
print("")
print("Addition:",num1 + num2) #บวก
print("Subtraction:",num1 - num2) #ลบ
print("Multiplication:",num1 * num2) #คูณ
print("Division: %.2f"%(num1 / num2)) #หาร
print("Division and floor:",num1 // num2) #หารปัดเศษ
print("Modulo:",num1 % num2) #หารเอาเศษ
print("Power:",num1 ** num2) #ยกกำลัง
